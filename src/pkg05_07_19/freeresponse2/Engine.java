package pkg05_07_19.freeresponse2;

public class Engine {
    private double weight, maxWeight;
    
    public Engine(double wt, double maxWt) {
        weight = wt;
        maxWeight = maxWt;
    }
    
    public double getWeight() {
        return weight;
    }
    
    public double getMaxWeight() {
        return maxWeight;
    }
}